/**
 * Class Max решение задачи 3.2 Максимум из двух чисел.
 *
 * @author Alexey Tomskih (alexey.tomskih@gmail.com)
 * @version $Id$
 * @since 22.12.2016
 */
public class Max {
    public int maxNumber(int first, int second) {
        return first > second ? first : second;
    }
}
